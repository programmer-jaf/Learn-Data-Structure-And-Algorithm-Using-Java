public class Main {

  // Define a method
  static void sayHello() {
    System.out.println("Say, Hello");
  }

  public static void main(String[] args) {
    System.out.println("Learn about Methods in Java");

    // What are Methods?
    /*
     * Methods are blocks of code that perform a specific task.
     * They only run when called.
     *
     * ✅ Benefits of Methods:
     *   1. Save time
     *   2. Reduce duplication
     *   3. Write once, use anywhere
     *
     * ✅ Method Syntax:
     *   returnType methodName(parameters) {
     *     // code
     *   }
     *
     * Example:
     *   void sayHello() {
     *     System.out.println("Hello");
     *   }
     */

    // Call the method
    sayHello();
  }
}
